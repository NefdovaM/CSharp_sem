const int rows = 2;
const int columns = 2;
const int tableNumber = 2;
int[,,] GetRandomMatrix(int rows, int columns, int tableNumber)
{
    int[,,] matrix = new int[rows, columns, tableNumber];
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            for (int z = 0; z < matrix.GetLength(2); z++)
            {
                matrix[i, j, z] = Random.Shared.Next (9, 100);
            }
        }
    }
    return matrix;
}

void PrintMatrix(int[,,] matrix)
{
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            for (int z = 0; z < matrix.GetLength(2); z++)
            {
                Console.Write($"{matrix[i, j, z]}({i},{j},{z})\t");
            }
                Console.WriteLine();
        }

    }
}

int[,,] myMatrix = GetRandomMatrix(rows, columns, tableNumber);
PrintMatrix(myMatrix);